package 储存学生对象;

public class Student_7 {
    private String name;
    private int age;

    public void Student() {
    }

    public void Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    //  ------------------------

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }
}
